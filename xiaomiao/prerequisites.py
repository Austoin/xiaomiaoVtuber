def _clean_role_list(items: list) -> list:
    return [item for item in items if item]


def update_role_lists(
    user_id: str, role: str, sisters: list, jhq: list, programmers: list
) -> tuple[list, list, list]:
    sisters = _clean_role_list([item for item in sisters if item != user_id])
    jhq = _clean_role_list([item for item in jhq if item != user_id])
    programmers = _clean_role_list([item for item in programmers if item != user_id])

    if role == "sister":
        sisters.append(user_id)
    elif role == "mother":
        jhq.append(user_id)
    elif role == "programmer":
        programmers.append(user_id)

    return sisters, jhq, programmers


def select_role(user_id: str, sisters: list, jhq: list, programmers: list) -> str:
    if user_id in programmers:
        return "programmer"
    if user_id in jhq:
        return "mother"
    if user_id in sisters:
        return "sister"
    return "programmer"


def plain_text_reply(text: str) -> str:
    replacements = {
        "```": "",
        "##": "",
        "**": "",
        "`": "",
    }
    for marker, replacement in replacements.items():
        text = text.replace(marker, replacement)
    return text


